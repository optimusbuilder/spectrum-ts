#!/usr/bin/env bun
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const here = dirname(fileURLToPath(import.meta.url));
const packageRoot = join(here, "..");
const repoRoot = join(packageRoot, "..", "..");

const pkg = (await Bun.file(join(packageRoot, "package.json")).json()) as {
  version: string;
};

const publishing = process.argv.includes("--publish");
const buildEnv: "development" | "production" = publishing
  ? "production"
  : "development";
const sdkVersion = publishing ? pkg.version : "local";

const buildEnvFile = `// This file is generated by scripts/prepare-package.ts. Do not edit.
//
// Locally and during \`bun run build\` this file reads:
//   SPECTRUM_SDK_VERSION = "local"
//   SPECTRUM_BUILD_ENV   = "development"
// The \`prepack\` script (run by \`bun publish\`) overwrites both with the real
// package.json#version and "production" before tsup compiles, then resets
// them back so the working tree stays clean.
export const SPECTRUM_SDK_VERSION = "${sdkVersion}";
export const SPECTRUM_BUILD_ENV: "development" | "production" = "${buildEnv}";
`;

await Bun.write(join(packageRoot, "src/build-env.ts"), buildEnvFile);
await Bun.write(
  join(packageRoot, "README.md"),
  Bun.file(join(repoRoot, "README.md"))
);
await Bun.write(
  join(packageRoot, "LICENSE"),
  Bun.file(join(repoRoot, "LICENSE"))
);

console.log(`Prepared spectrum-ts v${sdkVersion} (env=${buildEnv})`);
